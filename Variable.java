package Todo_Code;

public class Variable {
	
	@SuppressWarnings("unused")
	public static void main(String[] args) {
		
		//Tipo de variables
		int numero = 5; //int son variables de tipo numero entero (1 , 2 , 3 , 4 , 5 ...)
		String nombre = "Ibra"; //String son variables de tipo cadenade caracteres (perro , gato , maraco69 ...)
		char letra = 'l'; //char son variables de tipo caracter, solo letras (a , b , c , d ...)
		double temperatura = 38.5; //double son variables de tipo numero decimal (1.2 , 25.15 , 100.2 ...)
		boolean siOno = false; //boolean son variables de tipo logico: verdadero o falso (true , false)
		long largo = 1234567897; //long son variables de tipo numero pero de numeros largos (9999999999999999999)
	
	}
}
